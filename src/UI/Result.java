    private int lookID(String id) {
        for (int i = 0; i < questList.size(); i++) {
            if (id.equals(questList.get(i).getQuestID())) {
                return i;
            }
        }
        return -1;
    }

    private double calMark() {

        double correct = 0;

        txtArea.setText("Các câu sai của bạn là:\n");
        for (int i = 0; i < answerList.size(); i++) {
            int index = lookID(answerList.get(i).getAnswerID());
            Question temp = questList.get(index);
            if (index != -1) {
                if (answerList.get(i).getStudentAnswer().equals(temp.getQuestKey())) {
                    correct += 1;
                } else if (!answerList.get(i).getStudentAnswer().equals(temp.getQuestKey()) && !answerList.get(i).getStudentAnswer().equals("")) {
                    txtArea.append("Câu " + (index + 1) + "\n");
                }
            }
        }
        txtArea.append("Các câu chưa trả lời của bạn là:\n");
        for (int i = 0; i < answerList.size(); i++) {
            if (answerList.get(i).getStudentAnswer().equals("")) {
                int index = lookID(answerList.get(i).getAnswerID());
                txtArea.append("Câu " + (index + 1) + "\n");
            }
        }
        double markperquest = 10.0 / questList.size();
        return correct * markperquest;
    }

    private void showStatus(double mark) {
        if (mark < 5) {
            lbStatus.setText("Bạn cần cố gắng nhiều hơn nhé !!!");
        }
        if (mark >= 5 && mark < 7) {
            lbStatus.setText("Cố gắng lấy điểm cao hơn nữa nào !!!");
        }
        if (mark >= 7 && mark < 8) {
            lbStatus.setText("Một chút nữa thôi !!!");
        }
        if (mark >= 8 && mark < 10) {
            lbStatus.setText("Cố gắng lấy điểm 10 lần sau nhé !!!");
        }
        if (mark == 10) {
            lbStatus.setText("Rất tốt rồi luôn duy trì điểm số này nhé !!!");
        }
    }

