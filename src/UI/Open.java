    private String path;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Data file", "DAT");
        fChooser.setFileFilter(filter);
        fChooser.setMultiSelectionEnabled(false);
        int choice = fChooser.showOpenDialog(this);
        if (choice == JFileChooser.APPROVE_OPTION) {
            path = fChooser.getSelectedFile().getAbsolutePath();
            lbPath.setText(path);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Objectstream os = new Objectstream();
        try {
            Main.currentTopic = os.readDatfile(path);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (Main.currentTopic == null) {
            JOptionPane.showMessageDialog(this, "Bạn cần chọn file trước", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtKey.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn cần điền vào key", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (txtKey.getText().equals(Main.currentTopic.getTopicCode())) {
            studentName = txtName.getText();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Sai key", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

