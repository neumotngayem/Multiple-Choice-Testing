    private void txtNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 10) {
            Objectstream os = new Objectstream();
            try {
                Main.currentTopic = os.readDatfile(path);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (Main.currentTopic == null) {
                JOptionPane.showMessageDialog(this, "Bạn cần chọn file trước", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtKey.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn cần điền vào key", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (txtName.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn cần điền vào tên", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (txtKey.getText().equals(Main.currentTopic.getTopicCode())) {
                studentName = txtName.getText();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Sai key", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_txtNameKeyPressed

